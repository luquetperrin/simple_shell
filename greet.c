#include <stdio.h>

/**
 * greet - Prints a greeting message
 *
 * Description: This function prints "Hello, world!" to the console.
 */
void greet(void)
{
	printf("Hello, world!\n");
}

/**
 * main - Entry point of the program
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	greet();
	return (0);
}
